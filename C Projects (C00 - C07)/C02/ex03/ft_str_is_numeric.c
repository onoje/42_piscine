int	ft_str_is_numeric(char *str)
{
	int	x;

	x = 0;
	if (str[x] == '\0')
	{
		return (1);
	}
	while (str[x])
	{
		if (!(str[x] >= 48 && str[x] <= 57))
		{
			return (0);
		}
		else
		{
			x++;
		}
	}
	return (1);
}